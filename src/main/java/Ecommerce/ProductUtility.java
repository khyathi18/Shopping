package Ecommerce;

public class ProductUtility {
	public int mul(int a,int b)
	{
		return a*b;
	}
	public int sum(int a,int b)
	{
		return a+b;
	}
	public int difference(int a,int b)
	{
		return a-b;
	}
	public int quotient(int a,int b)
	{
		return a/b;
	}
	public int reminder(int a,int b)
	{
		return a%b;
	}
}
